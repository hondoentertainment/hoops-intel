#!/usr/bin/env node
// generate-projections.mjs — Weekly Rest-of-Season Projections generator
// Run manually or via GitHub Actions once per week (e.g., Monday 6am PST)
//
// Usage:
//   node scripts/generate-projections.mjs
//   node scripts/generate-projections.mjs --week "Week of March 23–29, 2026"

import Anthropic from "@anthropic-ai/sdk";
import { readFileSync, writeFileSync } from "fs";
import { fileURLToPath } from "url";
import { dirname, join } from "path";

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const ROOT = join(__dirname, "..");

// ── CLI args ────────────────────────────────────────────────

function getArg(flag) {
  const idx = process.argv.indexOf(flag);
  if (idx !== -1 && process.argv[idx + 1]) {
    return process.argv[idx + 1];
  }
  return null;
}

// Compute default week label: "Week of <Mon>–<Sun>, YYYY"
function defaultWeekLabel() {
  const now = new Date(new Date().toLocaleString("en-US", { timeZone: "America/Los_Angeles" }));
  const dayOfWeek = now.getDay();
  const daysToMon = dayOfWeek === 0 ? -6 : 1 - dayOfWeek;
  const mon = new Date(now);
  mon.setDate(now.getDate() + daysToMon);
  const sun = new Date(mon);
  sun.setDate(mon.getDate() + 6);

  const fmt = (d) =>
    d.toLocaleDateString("en-US", { month: "long", day: "numeric" });
  const year = sun.getFullYear();
  return `Week of ${fmt(mon)}–${fmt(sun).replace(/\D.*/, sun.getDate().toString())}, ${year}`;
}

function displayDate() {
  return new Date(
    new Date().toLocaleString("en-US", { timeZone: "America/Los_Angeles" })
  ).toLocaleDateString("en-US", { month: "long", day: "numeric", year: "numeric" });
}

// ── Read pulseData.ts as context ────────────────────────────

function readPulseContext() {
  const pulseDataPath = join(ROOT, "client", "src", "lib", "pulseData.ts");
  try {
    const raw = readFileSync(pulseDataPath, "utf8");
    // Extract standings / team records from pulseIndex
    const match = raw.match(/export const pulseIndex\s*=\s*(\[[\s\S]*?\]);/);
    if (match) {
      return `Current Pulse Index (includes team records):\n${match[1]}`;
    }
    return raw.slice(0, 3000);
  } catch (err) {
    console.warn("⚠  Could not read pulseData.ts:", err.message);
    return "(pulseData.ts not available)";
  }
}

// ── Claude prompt ───────────────────────────────────────────

function buildPrompt(weekLabel, pulseContext) {
  return `You are the Hoops Intel AI projections analyst. Generate the weekly Rest-of-Season Projections for all 30 NBA teams plus a complete projected playoff bracket.

Context: ${pulseContext}

Week: ${weekLabel}

Return ONLY a valid TypeScript module with this exact structure — no markdown fences, no commentary, just the TypeScript:

// Auto-generated by generate-projections.mjs
// ${weekLabel}

export interface TeamProjection {
  team: string;
  conference: "east" | "west";
  currentWins: number;
  currentLosses: number;
  projectedWins: number;
  projectedLosses: number;
  winChange: number;
  playoffProb: number;
  championshipProb: number;
  remainingSOS: number;
  remainingSOSLabel: string;
  keyStretch: string;
  projection: string;
  narrative: string;
}

export interface PlayoffMatchup {
  round: string;
  highSeed: string;
  lowSeed: string;
  seriesProb: string;
  keyFactor: string;
}

export interface ProjectionsData {
  generatedDate: string;
  weekLabel: string;
  teams: TeamProjection[];
  projectedBracket: {
    east: PlayoffMatchup[];
    west: PlayoffMatchup[];
    finals: PlayoffMatchup;
  };
  biggestRiser: { team: string; change: string; reason: string };
  biggestFaller: { team: string; change: string; reason: string };
  weeklyNarrative: string;
}

export const projectionsData: ProjectionsData = {
  generatedDate: "${displayDate()}",
  weekLabel: "${weekLabel}",
  weeklyNarrative: "...",
  biggestRiser: { team: "...", change: "...", reason: "..." },
  biggestFaller: { team: "...", change: "...", reason: "..." },
  teams: [
    // ALL 30 NBA teams
    // conference: "east" or "west"
    // currentWins/currentLosses: based on context
    // projectedWins/projectedLosses: must total 82
    // winChange: change from hypothetical last week's projection (-5 to +5 range)
    // playoffProb: 0-100
    // championshipProb: 0-100 with 0.1 precision (must sum to ~100 across all teams)
    // remainingSOS: 1-30 (1 = hardest remaining schedule)
    // remainingSOSLabel: "Brutal" | "Tough" | "Average" | "Easy" | "Cupcake"
    // keyStretch: upcoming schedule note
    // projection: "1st Round Exit" | "2nd Round" | "Conference Finals" | "Finals" | "Champion"
    // narrative: 2-3 sentences specific to this week
  ],
  projectedBracket: {
    east: [
      // 4 First Round matchups
      // 2 Second Round matchups
      // 1 Conference Finals matchup
    ],
    west: [
      // 4 First Round matchups
      // 2 Second Round matchups
      // 1 Conference Finals matchup
    ],
    finals: {
      // 1 NBA Finals matchup
    },
  },
};

Rules:
- Include ALL 30 NBA teams (15 East, 15 West)
- Current records must be consistent with the pulse data context
- Projected records must total 82 games (wins + losses = 82)
- Championship probabilities should roughly sum to 100% across all teams
- The playoff bracket must be consistent with the projected standings
- seriesProb format: "TEAM in X" (e.g., "BOS in 6")
- keyFactor: 1 sentence explaining why the series goes that way
- remainingSOS rankings must be unique (1-30, no ties)
- Use 3-letter team abbreviations consistently
- Narratives must reference THIS week's context
- The TypeScript must be syntactically valid and importable`;
}

// ── Write output ────────────────────────────────────────────

function writeOutput(content) {
  const outPath = join(ROOT, "client", "src", "lib", "projectionsData.ts");
  writeFileSync(outPath, content, "utf8");
  return outPath;
}

// ── Main ────────────────────────────────────────────────────

async function main() {
  const weekLabel = getArg("--week") ?? defaultWeekLabel();
  const generatedDate = displayDate();

  console.log("🏀 Hoops Intel — Rest-of-Season Projections Generator");
  console.log(`   Week:      ${weekLabel}`);
  console.log(`   Generated: ${generatedDate}`);
  console.log("");

  const pulseContext = readPulseContext();
  console.log("📊 Loaded pulseData.ts context");

  const client = new Anthropic();

  console.log("🤖 Calling Claude (claude-sonnet-4-6)...");
  const prompt = buildPrompt(weekLabel, pulseContext);

  let responseText;
  try {
    const message = await client.messages.create({
      model: "claude-sonnet-4-6",
      max_tokens: 16000,
      messages: [
        {
          role: "user",
          content: prompt,
        },
      ],
    });

    const block = message.content.find((b) => b.type === "text");
    if (!block) throw new Error("No text block in Claude response");
    responseText = block.text;
  } catch (err) {
    console.error("❌ Claude API error:", err.message);
    process.exit(1);
  }

  // Strip any accidental markdown fences
  responseText = responseText
    .replace(/^```(?:typescript|ts)?\n?/m, "")
    .replace(/\n?```$/m, "")
    .trim();

  let outPath;
  try {
    outPath = writeOutput(responseText);
  } catch (err) {
    console.error("❌ Failed to write output:", err.message);
    process.exit(1);
  }

  console.log(`✅ Rest-of-Season Projections written to: ${outPath}`);
  console.log("");
  console.log("Next steps:");
  console.log("  1. Review client/src/lib/projectionsData.ts for accuracy");
  console.log("  2. Commit and deploy");
}

main().catch((err) => {
  console.error("Fatal error:", err);
  process.exit(1);
});
