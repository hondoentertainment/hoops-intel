#!/usr/bin/env node
// generate-sentiment.mjs — Daily Social Sentiment Analysis generator
// Uses Claude AI to simulate social media sentiment based on game results/narratives
// Run daily at 5am PST via GitHub Actions (.github/workflows/daily-update.yml)

import Anthropic from "@anthropic-ai/sdk";
import { readFileSync, writeFileSync } from "fs";
import { fileURLToPath } from "url";
import { dirname, join } from "path";

import { toESPNDate, toISODate, toDisplayDate } from "./lib/dates.mjs";

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const ROOT = join(__dirname, "..");

// ── Read pulse context ─────────────────────────────────────
function readPulseContext() {
  try {
    return readFileSync(join(ROOT, "client/src/lib/pulseData.ts"), "utf8");
  } catch {
    return "(pulseData.ts not available)";
  }
}

// ── Main ──────────────────────────────────────────────────
async function main() {
  const client = new Anthropic();

  const todayISO = toISODate(0);
  const todayDisplay = toDisplayDate(0);
  const yesterdayESPN = toESPNDate(-1);

  console.log(`💬 Generating Sentiment Analysis for ${todayDisplay}`);

  const pulseContext = readPulseContext();
  console.log("📊 Loaded pulseData.ts context");

  // ── Generate sentimentData.ts ───────────────────────────
  console.log("🤖 Calling Claude API to generate sentiment analysis...");

  const prompt = `You are the Hoops Intel social media analyst. Generate today's Social Sentiment analysis — simulating what NBA Twitter/social media is buzzing about based on last night's games and current league narratives.

## Current Pulse Data (for context on last night's games and standings)
${pulseContext}

## Date Info
- Date: ${todayDisplay}
- ISO: ${todayISO}
- Games analyzed from: ${yesterdayESPN}

Generate a complete TypeScript file (sentimentData.ts) with this EXACT structure:

// Auto-generated by generate-sentiment.mjs
// ${todayDisplay}

export interface SentimentTopic {
  id: string;
  topic: string;
  sentiment: "positive" | "negative" | "mixed" | "neutral";
  intensity: number;
  volume: number;
  summary: string;
  sampleTakes: string[];
  relatedPlayers: string[];
  relatedTeams: string[];
  trending: boolean;
}

export interface TeamSentiment {
  team: string;
  teamFull: string;
  sentiment: "positive" | "negative" | "mixed" | "neutral";
  score: number;
  buzzWords: string[];
  narrativeSummary: string;
}

export interface PlayerSentiment {
  player: string;
  team: string;
  sentiment: "positive" | "negative" | "mixed" | "neutral";
  score: number;
  mentions: number;
  topTake: string;
  narrativeArc: string;
}

export interface SentimentData {
  generatedDate: string;
  displayDate: string;
  overallMood: string;
  topics: SentimentTopic[];
  teamSentiments: TeamSentiment[];
  playerSentiments: PlayerSentiment[];
  hottestTake: string;
  coldestTake: string;
}

export const sentimentData: SentimentData = {
  generatedDate: "${todayISO}",
  displayDate: "${todayDisplay}",
  overallMood: "...",
  topics: [ ... ],       // 6-8 trending topics
  teamSentiments: [ ... ],  // 8-10 teams generating the most buzz
  playerSentiments: [ ... ], // 8-10 players generating the most buzz
  hottestTake: "The most fire/controversial take circulating",
  coldestTake: "The worst/most freezing cold take circulating",
};

## Guidelines
- id: kebab-case slug (e.g. "lakers-losing-streak")
- intensity: 1-100 (how heated the discourse is)
- volume: simulated mention count (100-50000 range)
- score: -100 to 100 (negative = negative sentiment, positive = positive)
- mentions: simulated mention count for player (100-25000)
- sampleTakes: 3-4 realistic-sounding social media takes (short, punchy, opinionated)
- Write in an authentic social media voice — hot takes, overreactions, memes
- Base everything on the actual game results and standings from pulseData
- trending: true for the top 3 most-discussed topics
- overallMood: one phrase capturing the league vibe ("Playoff race chaos", "Trade deadline buzz", etc.)

Output ONLY the complete TypeScript file. No markdown fences, no explanation.`;

  const msg = await client.messages.create({
    model: "claude-sonnet-4-6",
    max_tokens: 8192,
    messages: [{ role: "user", content: prompt }],
  });

  let content = msg.content[0].text.trim();

  // Strip accidental markdown fences
  content = content
    .replace(/^```(?:typescript|ts)?\n?/m, "")
    .replace(/\n?```$/m, "")
    .trim();

  writeFileSync(join(ROOT, "client/src/lib/sentimentData.ts"), content, "utf8");
  console.log("✓ sentimentData.ts written");
  console.log(`\n✅ Sentiment Analysis complete for ${todayDisplay}`);
}

main().catch((err) => {
  console.error("❌ Generation failed:", err);
  process.exit(1);
});
