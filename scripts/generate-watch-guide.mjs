#!/usr/bin/env node
// generate-watch-guide.mjs — Daily Watch Guide generator
// Ranks tonight's NBA games by entertainment value using Claude AI
// Run daily at 5am PST via GitHub Actions (.github/workflows/daily-update.yml)

import { claudeGenerate } from "./lib/claude-client.mjs";
import { readFileSync, writeFileSync } from "fs";
import { fileURLToPath } from "url";
import { dirname, join } from "path";
import { toESPNDate, toISODate, toDisplayDate } from "./lib/daily-dates.mjs";

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const ROOT = join(__dirname, "..");

// ── ESPN schedule API ──────────────────────────────────────
async function fetchESPN(espnDate) {
  const url = `https://site.api.espn.com/apis/site/v2/sports/basketball/nba/scoreboard?dates=${espnDate}`;
  const res = await fetch(url);
  if (!res.ok) throw new Error(`ESPN API ${url} returned ${res.status}`);
  return res.json();
}

function parseGames(espnData) {
  return (espnData.events || []).map((e) => {
    const comp = e.competitions[0];
    const home = comp.competitors.find((c) => c.homeAway === "home");
    const away = comp.competitors.find((c) => c.homeAway === "away");
    const done = comp.status?.type?.completed ?? false;

    return {
      status: done ? "final" : "scheduled",
      homeTeam: home?.team?.abbreviation ?? "",
      homeTeamFull: home?.team?.displayName ?? "",
      homeRecord: home?.records?.[0]?.summary ?? "",
      awayTeam: away?.team?.abbreviation ?? "",
      awayTeamFull: away?.team?.displayName ?? "",
      awayRecord: away?.records?.[0]?.summary ?? "",
      time: comp.status?.type?.shortDetail ?? "",
      venue: comp.venue?.fullName ?? "",
      tv: (comp.broadcasts || []).map((b) => b.names?.join(", ")).filter(Boolean).join(" / ") || "Local",
    };
  });
}

// ── Read pulse context ─────────────────────────────────────
function readPulseContext() {
  try {
    return readFileSync(join(ROOT, "client/src/lib/pulseData.ts"), "utf8").slice(0, 3000);
  } catch {
    return "(pulseData.ts not available)";
  }
}

// ── Main ──────────────────────────────────────────────────
async function main() {
  const todayESPN = toESPNDate(0);
  const todayISO = toISODate(0);
  const todayDisplay = toDisplayDate(0);

  console.log(`📺 Generating Watch Guide for ${todayDisplay}`);

  // Fetch today's schedule
  console.log(`🏀 Fetching ESPN schedule for ${todayESPN}...`);
  let games;
  try {
    const espnData = await fetchESPN(todayESPN);
    games = parseGames(espnData);
  } catch (err) {
    console.error("ESPN fetch error:", err.message);
    process.exit(1);
  }

  const scheduledGames = games.filter((g) => g.status !== "final");
  console.log(`   Found ${scheduledGames.length} scheduled games`);

  if (scheduledGames.length === 0) {
    console.log("No games scheduled today — writing empty watch guide.");
    const empty = `// Auto-generated by generate-watch-guide.mjs
// ${todayDisplay} — No games scheduled

export interface WatchGuideFactors {
  starPower: number;
  playoffImplications: number;
  rivalry: number;
  entertainment: number;
  storyline: number;
}

export interface WatchGuideGame {
  rank: number;
  awayTeam: string;
  awayTeamFull: string;
  awayRecord: string;
  homeTeam: string;
  homeTeamFull: string;
  homeRecord: string;
  time: string;
  tv: string;
  venue: string;
  entertainmentScore: number;
  starPower: number;
  playoffImplications: number;
  rivalry: number;
  storyline: string;
  keyMatchup: string;
  mustWatch: boolean;
  factors: WatchGuideFactors;
  watchScore: number;
  headline: string;
}

export interface WatchGuidePick {
  gameIndex: number;
  reason: string;
}

export interface WatchGuideData {
  generatedDate: string;
  displayDate: string;
  date: string;
  gamesCount: number;
  games: WatchGuideGame[];
  topPick: WatchGuidePick;
  sleeper: WatchGuidePick;
  skipIt: WatchGuidePick;
  nightOverview: string;
}

export const watchGuideData: WatchGuideData = {
  generatedDate: "${todayISO}",
  displayDate: "${todayDisplay}",
  date: "${todayDisplay}",
  gamesCount: 0,
  games: [],
  topPick: { gameIndex: 0, reason: "No games tonight" },
  sleeper: { gameIndex: 0, reason: "No games tonight" },
  skipIt: { gameIndex: 0, reason: "No games tonight" },
  nightOverview: "The NBA takes a night off. Rest up for tomorrow's action.",
};
`;
    writeFileSync(join(ROOT, "client/src/lib/watchGuideData.ts"), empty, "utf8");
    console.log("✓ watchGuideData.ts written (empty)");
    return;
  }

  const pulseContext = readPulseContext();

  // ── Generate watchGuideData.ts ──────────────────────────
  console.log("🤖 Calling Claude API to generate watch guide...");

  const prompt = `You are the Hoops Intel analyst. Generate tonight's Watch Guide — ranking all scheduled NBA games by entertainment value.

## Tonight's Schedule (${todayESPN}) — ESPN API
${JSON.stringify(scheduledGames, null, 2)}

## Current Pulse Context
${pulseContext}

## Date Info
- Date: ${todayDisplay}
- ISO: ${todayISO}

Generate a complete TypeScript file (watchGuideData.ts) with this EXACT structure:

// Auto-generated by generate-watch-guide.mjs
// ${todayDisplay}

export interface WatchGuideFactors {
  starPower: number;
  playoffImplications: number;
  rivalry: number;
  entertainment: number;
  storyline: number;
}

export interface WatchGuideGame {
  rank: number;
  awayTeam: string;
  awayTeamFull: string;
  awayRecord: string;
  homeTeam: string;
  homeTeamFull: string;
  homeRecord: string;
  time: string;
  tv: string;
  venue: string;
  entertainmentScore: number;
  starPower: number;
  playoffImplications: number;
  rivalry: number;
  storyline: string;
  keyMatchup: string;
  mustWatch: boolean;
  factors: WatchGuideFactors;
  watchScore: number;
  headline: string;
}

export interface WatchGuidePick {
  gameIndex: number;
  reason: string;
}

export interface WatchGuideData {
  generatedDate: string;
  displayDate: string;
  date: string;
  gamesCount: number;
  games: WatchGuideGame[];
  topPick: WatchGuidePick;
  sleeper: WatchGuidePick;
  skipIt: WatchGuidePick;
  nightOverview: string;
}

export const watchGuideData: WatchGuideData = {
  generatedDate: "${todayISO}",
  displayDate: "${todayDisplay}",
  date: "${todayDisplay}",
  gamesCount: N,
  games: [ ... ],
  topPick: { gameIndex: 0, reason: "Why the #1 game leads the night" },
  sleeper: { gameIndex: 0, reason: "Underrated game that could over-deliver" },
  skipIt: { gameIndex: 0, reason: "Lowest watch-score matchup or best to skip if short on time" },
  nightOverview: "2-3 sentence overview of tonight's slate",
};

## Scoring Guidelines
- entertainmentScore: 1-100 overall entertainment value (separate from watchScore)
- starPower, playoffImplications, rivalry: 1-10 descriptive inputs (mirror the nightly narrative scale)
- storyline: 1-2 sentence narrative hook for this matchup (string)
- factors: ALL FIVE keys 0-20 integers ONLY: starPower, playoffImplications, rivalry, entertainment, storyline. These power the UI factor bars. "storyline" here is a numeric hype score, not the storyline string.
- watchScore: MUST equal the sum of factors.starPower + factors.playoffImplications + factors.rivalry + factors.entertainment + factors.storyline (max 100). Set factors so this sum matches entertainmentScore as closely as possible (within 0-100).
- headline: one punchy H2-style line for the card (distinct from the long storyline string)
- topPick.gameIndex: index in games[] of the #1 ranked game (usually 0). sleeper and skipIt: valid indices into games[]; skipIt should reference the lowest watchScore game when possible.
- keyMatchup: "Player A vs. Player B" — the main individual battle to watch
- mustWatch: true for top 3 games or any game scoring 80+ entertainment
- Use canonical 3-letter team abbreviations as used in Hoops Intel (e.g. NYK, SAS, BRK, not NY or SA).

## Rules
- Rank ALL games from most to least entertaining
- Be specific about WHY each game matters tonight
- Consider current narratives, streaks, playoff race, injuries
- Output ONLY the complete TypeScript file. No markdown fences, no explanation.`;

  const msg = await claudeGenerate("watch guide", {
    max_tokens: 8192,
    messages: [{ role: "user", content: prompt }],
  });

  let content = msg.content[0].text.trim();

  // Strip accidental markdown fences
  content = content
    .replace(/^```(?:typescript|ts)?\n?/m, "")
    .replace(/\n?```$/m, "")
    .trim();

  writeFileSync(join(ROOT, "client/src/lib/watchGuideData.ts"), content, "utf8");
  console.log("✓ watchGuideData.ts written");
  console.log(`\n✅ Watch Guide complete for ${todayDisplay}`);
}

main().catch((err) => {
  console.error("❌ Generation failed:", err);
  process.exit(1);
});
