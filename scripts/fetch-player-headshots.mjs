#!/usr/bin/env node
// fetch-player-headshots.mjs — Build the player name → ESPN athlete id map.
//
// Fetches all 30 NBA rosters from ESPN and writes a normalized-name → athlete id
// map to client/src/lib/playerHeadshotData.ts. PlayerAvatar turns each id into a
// headshot CDN URL, falling back to a team-colored initials crest when a name is
// missing. Rosters change slowly, so this runs in the daily pipeline with
// continue-on-error — a fetch failure preserves the previously generated map.
//
// Output: client/src/lib/playerHeadshotData.ts

import { writeFileSync, readFileSync, existsSync } from "fs";
import { fileURLToPath } from "url";
import { dirname, join } from "path";
import { fetchWithRetry } from "./lib/retry.mjs";

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const ROOT = join(__dirname, "..");
const OUT = join(ROOT, "client", "src", "lib", "playerHeadshotData.ts");
const ESPN = "https://site.api.espn.com/apis/site/v2/sports/basketball/nba";

// Keep identical to normalizePlayerName in client/src/components/PlayerAvatar.tsx.
function normalizePlayerName(name) {
  return (name || "")
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .toLowerCase()
    .replace(/[.'’]/g, "")
    .replace(/\b(jr|sr|ii|iii|iv)\b/g, "")
    .replace(/[^a-z0-9]+/g, " ")
    .trim()
    .replace(/\s+/g, " ");
}

async function fetchJson(url) {
  const res = await fetchWithRetry(url, {}, { maxRetries: 3, baseDelayMs: 2000 });
  if (!res.ok) throw new Error(`${url} -> HTTP ${res.status}`);
  return res.json();
}

function collectAthletes(roster, sink) {
  const groups = Array.isArray(roster?.athletes) ? roster.athletes : [];
  for (const group of groups) {
    const items = Array.isArray(group?.items) ? group.items : group?.id ? [group] : [];
    for (const a of items) {
      const id = Number(a?.id);
      const name = a?.displayName ?? a?.fullName;
      if (Number.isFinite(id) && name) sink[normalizePlayerName(name)] = id;
    }
  }
}

async function main() {
  const teamsDoc = await fetchJson(`${ESPN}/teams`);
  const teams = teamsDoc?.sports?.[0]?.leagues?.[0]?.teams ?? [];
  const teamIds = teams.map((t) => t?.team?.id).filter(Boolean);
  if (teamIds.length === 0) throw new Error("no team ids returned from ESPN /teams");

  const map = {};
  let ok = 0;
  for (const id of teamIds) {
    try {
      const roster = await fetchJson(`${ESPN}/teams/${id}/roster`);
      collectAthletes(roster, map);
      ok += 1;
    } catch (err) {
      console.warn(`⚠ roster ${id} failed: ${err.message}`);
    }
  }

  const count = Object.keys(map).length;
  if (ok === 0 || count === 0) {
    console.error(`✗ No headshots resolved (rosters ok: ${ok}). Preserving existing file.`);
    process.exit(existsSync(OUT) ? 0 : 1);
  }

  const entries = Object.keys(map)
    .sort()
    .map((k) => `  ${JSON.stringify(k)}: ${map[k]},`)
    .join("\n");

  const body = `// GENERATED by scripts/fetch-player-headshots.mjs — do not edit by hand.
// Maps a normalized player name (see normalizePlayerName) to their ESPN athlete id.
// Refreshed by the daily pipeline; empty until the first roster fetch runs in CI,
// where PlayerAvatar falls back to a team-colored initials crest.
export const playerHeadshotIds: Record<string, number> = {
${entries}
};
`;

  const prev = existsSync(OUT) ? readFileSync(OUT, "utf8") : null;
  if (prev === body) {
    console.log(`✓ Player headshots unchanged (${count} players, ${ok}/${teamIds.length} rosters).`);
    return;
  }
  writeFileSync(OUT, body, "utf8");
  console.log(`✓ Wrote ${count} player headshots (${ok}/${teamIds.length} rosters) to ${OUT}`);
}

main().catch((err) => {
  console.error(`✗ fetch-player-headshots failed: ${err.message}`);
  process.exit(existsSync(OUT) ? 0 : 1);
});
