#!/usr/bin/env node
// generate-trade-value.mjs — Weekly Trade Value Index generator
// Run manually or via GitHub Actions once per week (e.g., Monday 6am PST)
//
// Usage:
//   node scripts/generate-trade-value.mjs
//   node scripts/generate-trade-value.mjs --week "Week of March 23–29, 2026"

import Anthropic from "@anthropic-ai/sdk";
import { readFileSync, writeFileSync } from "fs";
import { fileURLToPath } from "url";
import { dirname, join } from "path";

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const ROOT = join(__dirname, "..");

// ── CLI args ────────────────────────────────────────────────

function getArg(flag) {
  const idx = process.argv.indexOf(flag);
  if (idx !== -1 && process.argv[idx + 1]) {
    return process.argv[idx + 1];
  }
  return null;
}

// Compute default week label: "Week of <Mon>–<Sun>, YYYY"
function defaultWeekLabel() {
  const now = new Date(new Date().toLocaleString("en-US", { timeZone: "America/Los_Angeles" }));
  // Find Monday of current week (day 0=Sun, 1=Mon … 6=Sat)
  const dayOfWeek = now.getDay(); // 0=Sun
  const daysToMon = dayOfWeek === 0 ? -6 : 1 - dayOfWeek;
  const mon = new Date(now);
  mon.setDate(now.getDate() + daysToMon);
  const sun = new Date(mon);
  sun.setDate(mon.getDate() + 6);

  const fmt = (d) =>
    d.toLocaleDateString("en-US", { month: "long", day: "numeric" });
  const year = sun.getFullYear();
  return `Week of ${fmt(mon)}–${fmt(sun).replace(/\D.*/, sun.getDate().toString())}, ${year}`;
}

function displayDate() {
  return new Date(
    new Date().toLocaleString("en-US", { timeZone: "America/Los_Angeles" })
  ).toLocaleDateString("en-US", { month: "long", day: "numeric", year: "numeric" });
}

// ── Read pulseData.ts as context ────────────────────────────

function readPulseContext() {
  const pulseDataPath = join(ROOT, "client", "src", "lib", "pulseData.ts");
  try {
    const raw = readFileSync(pulseDataPath, "utf8");
    // Extract pulseIndex block for context (lines between the export and the closing bracket)
    const match = raw.match(/export const pulseIndex\s*=\s*(\[[\s\S]*?\]);/);
    if (match) {
      return `Current Pulse Index (top 10 players by today's performance score):\n${match[1]}`;
    }
    // Fallback: pass the first 3000 chars
    return raw.slice(0, 3000);
  } catch (err) {
    console.warn("⚠  Could not read pulseData.ts:", err.message);
    return "(pulseData.ts not available)";
  }
}

// ── Claude prompt ───────────────────────────────────────────

function buildPrompt(weekLabel, pulseContext) {
  return `You are the Hoops Intel AI analyst. Generate the weekly Trade Value Index (TVI) — a ranking of the top 30 most tradeable NBA players.

Context: ${pulseContext}

Week: ${weekLabel}

The TVI scores players 0–100 on their real-world trade value to an acquiring team right now.
Key inputs: recent performance trend, contract (years remaining + AAV), age, team's playoff position, injury history.

Return ONLY a valid TypeScript module with this exact structure — no markdown fences, no commentary, just the TypeScript:

// Auto-generated by generate-trade-value.mjs
// ${weekLabel}

export interface TVIPlayer {
  rank: number;
  prevRank: number;
  player: string;
  team: string;
  age: number;
  contract: string;
  tradeValue: number;
  trend: "up" | "down" | "stable";
  rationale: string;
}

export interface TVIData {
  generatedDate: string;
  weekLabel: string;
  players: TVIPlayer[];
}

export const tradeValueData: TVIData = {
  generatedDate: "${displayDate()}",
  weekLabel: "${weekLabel}",
  players: [
    // ... 30 players, each with:
    // rank (1–30), prevRank (last week's rank), player (full name), team (3-letter abbr),
    // age (integer), contract (e.g. "3 yrs / $120M remaining"),
    // tradeValue (0–100 integer), trend ("up"|"down"|"stable"),
    // rationale (2–3 sentences, editorial voice, specific to this week)
  ],
};

Rules:
- Include exactly 30 players
- prevRank should reflect a plausible last week's ranking (can vary by 1–5 spots from current)
- Rationale must be specific to THIS week's news, injuries, team context — not generic
- Contract format: "X yrs / $YYM remaining" or "X yrs / $YYM remaining (rookie)" for rookie deals
- tradeValue: 95–100 for generational stars on good contracts, lower for aging/expensive/injured
- The TypeScript must be syntactically valid and importable`;
}

// ── Write output ────────────────────────────────────────────

function writeOutput(content) {
  const outPath = join(ROOT, "client", "src", "lib", "tradeValueData.ts");
  writeFileSync(outPath, content, "utf8");
  return outPath;
}

// ── Main ────────────────────────────────────────────────────

async function main() {
  const weekLabel = getArg("--week") ?? defaultWeekLabel();
  const generatedDate = displayDate();

  console.log("🏀 Hoops Intel — Trade Value Index Generator");
  console.log(`   Week:      ${weekLabel}`);
  console.log(`   Generated: ${generatedDate}`);
  console.log("");

  // Read pulse context
  const pulseContext = readPulseContext();
  console.log("📊 Loaded pulseData.ts context");

  // Init Anthropic client
  const client = new Anthropic();

  console.log("🤖 Calling Claude (claude-sonnet-4-6)...");
  const prompt = buildPrompt(weekLabel, pulseContext);

  let responseText;
  try {
    const message = await client.messages.create({
      model: "claude-sonnet-4-6",
      max_tokens: 8192,
      messages: [
        {
          role: "user",
          content: prompt,
        },
      ],
    });

    const block = message.content.find((b) => b.type === "text");
    if (!block) throw new Error("No text block in Claude response");
    responseText = block.text;
  } catch (err) {
    console.error("❌ Claude API error:", err.message);
    process.exit(1);
  }

  // Strip any accidental markdown fences
  responseText = responseText
    .replace(/^```(?:typescript|ts)?\n?/m, "")
    .replace(/\n?```$/m, "")
    .trim();

  // Write to file
  let outPath;
  try {
    outPath = writeOutput(responseText);
  } catch (err) {
    console.error("❌ Failed to write output:", err.message);
    process.exit(1);
  }

  console.log(`✅ Trade Value Index written to: ${outPath}`);
  console.log("");
  console.log("Next steps:");
  console.log("  1. Review client/src/lib/tradeValueData.ts for accuracy");
  console.log("  2. Update client/src/pages/TradeValue.tsx to import from tradeValueData");
  console.log("  3. Commit and deploy");
}

main().catch((err) => {
  console.error("Fatal error:", err);
  process.exit(1);
});
