#!/usr/bin/env node
// sync-line-movement.mjs — Morning opener snapshot + closing spread sync for betting intel.
//
//   node scripts/sync-line-movement.mjs snapshot   # after edition — freeze openers
//   node scripts/sync-line-movement.mjs sync       # midday/scores — write lineMovementData.ts

import { readFileSync, writeFileSync, existsSync, mkdirSync } from "fs";
import { fileURLToPath, pathToFileURL } from "url";
import { dirname, join } from "path";
import { extractPulseExport } from "./lib/read-pulse-exports.mjs";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");
const OPENERS = join(ROOT, ".daily-data", "line-openers.json");
const ARCHIVE = join(ROOT, ".daily-data", "line-openers-archive.json");
const EXTERNAL_ODDS = join(ROOT, ".daily-data", "odds-external.json");
const OUT = join(ROOT, "client/src/lib/lineMovementData.ts");

export function matchupKey(away, home) {
  return [away, home].sort().join("-");
}

/** Optional manual/sportsbook feed — see scripts/fetch-line-odds.mjs */
export function loadExternalOdds() {
  if (!existsSync(EXTERNAL_ODDS)) return null;
  try {
    return JSON.parse(readFileSync(EXTERNAL_ODDS, "utf8"));
  } catch (err) {
    console.warn(`⚠ Could not parse ${EXTERNAL_ODDS}: ${err.message}`);
    return null;
  }
}

export function mergeExternalSpreads(previews, external) {
  const games = external?.games;
  if (!Array.isArray(games) || games.length === 0) return previews;
  const map = new Map(
    games
      .filter((g) => g?.awayTeam && g?.homeTeam)
      .map((g) => [matchupKey(g.awayTeam, g.homeTeam), g]),
  );
  if (map.size === 0) return previews;
  return previews.map((g) => {
    const ext = map.get(matchupKey(g.awayTeam, g.homeTeam));
    if (!ext) return g;
    const spread = ext.closingSpread ?? ext.spread;
    if (!spread) return g;
    return { ...g, spread: String(spread) };
  });
}

function snapshotOpeners() {
  const edition = extractPulseExport("pulseEdition");
  const previews = extractPulseExport("gamePreviews") || [];
  const date = edition?.date ?? new Date().toISOString().slice(0, 10);
  const games = previews.map((g) => ({
    awayTeam: g.awayTeam,
    homeTeam: g.homeTeam,
    openingSpread: g.openingSpread || g.spread || "",
    spreadAtOpen: g.spread || "",
    capturedAt: new Date().toISOString(),
  }));
  mkdirSync(join(ROOT, ".daily-data"), { recursive: true });
  const snap = { editionDate: date, games, capturedAt: new Date().toISOString() };
  writeFileSync(OPENERS, JSON.stringify(snap, null, 2), "utf8");

  let archive = { editions: [] };
  if (existsSync(ARCHIVE)) {
    try {
      archive = JSON.parse(readFileSync(ARCHIVE, "utf8"));
    } catch {
      archive = { editions: [] };
    }
  }
  const editions = Array.isArray(archive.editions) ? archive.editions : [];
  const ix = editions.findIndex((e) => e.editionDate === date);
  const entry = { editionDate: date, games, capturedAt: snap.capturedAt };
  if (ix >= 0) editions[ix] = entry;
  else editions.push(entry);
  editions.sort((a, b) => String(b.editionDate).localeCompare(String(a.editionDate)));
  writeFileSync(ARCHIVE, JSON.stringify({ editions: editions.slice(0, 90) }, null, 2), "utf8");
  console.log(`✅ Snapshotted ${games.length} line openers for ${date} (+ archive)`);
}

export function writeLineMovementTs(movements, editionDate) {
  const rows =
    movements.length === 0
      ? ""
      : movements
          .map(
            (m) =>
              `  {\n    awayTeam: ${JSON.stringify(m.awayTeam)},\n    homeTeam: ${JSON.stringify(m.homeTeam)},\n    openingSpread: ${JSON.stringify(m.openingSpread)},\n    closingSpread: ${JSON.stringify(m.closingSpread)},\n    updatedAt: ${JSON.stringify(m.updatedAt)},\n  }`,
          )
          .join(",\n");

  const content = `// AUTO-GENERATED by scripts/sync-line-movement.mjs — do not edit by hand.

export interface LineMovementRow {
  awayTeam: string;
  homeTeam: string;
  openingSpread: string;
  closingSpread: string;
  updatedAt: string;
}

export const lineMovementEditionDate = ${JSON.stringify(editionDate ?? "")};

export const lineMovementRows: LineMovementRow[] = [
${rows}
];
`;
  writeFileSync(OUT, content, "utf8");
  console.log(`✅ Wrote ${movements.length} line movement rows to lineMovementData.ts`);
}

/**
 * @param {{ useExternal?: boolean }} [opts]
 */
export function syncLineClosers(opts = {}) {
  const edition = extractPulseExport("pulseEdition");
  let previews = extractPulseExport("gamePreviews") || [];
  if (opts.useExternal) {
    const external = loadExternalOdds();
    if (external) {
      previews = mergeExternalSpreads(previews, external);
      console.log(`📈 Merged ${external.games?.length ?? 0} external odds row(s) from odds-external.json`);
    }
  }
  let openers = { editionDate: edition?.date, games: [] };
  if (existsSync(OPENERS)) {
    try {
      openers = JSON.parse(readFileSync(OPENERS, "utf8"));
    } catch (err) {
      console.warn(`⚠ Could not parse ${OPENERS}: ${err.message}`);
    }
  }
  const openMap = new Map((openers.games || []).map((g) => [matchupKey(g.awayTeam, g.homeTeam), g]));
  const now = new Date().toISOString();
  const movements = previews.map((g) => {
    const key = matchupKey(g.awayTeam, g.homeTeam);
    const open = openMap.get(key);
    return {
      awayTeam: g.awayTeam,
      homeTeam: g.homeTeam,
      openingSpread: open?.openingSpread || g.openingSpread || g.spread || "",
      closingSpread: g.spread || "",
      updatedAt: now,
    };
  });
  writeLineMovementTs(movements, edition?.date ?? openers.editionDate);
}

function syncClosers() {
  syncLineClosers({ useExternal: existsSync(EXTERNAL_ODDS) });
}

const isMain = import.meta.url === pathToFileURL(process.argv[1] ?? "").href;

if (isMain) {
  const cmd = process.argv[2] || "sync";
  if (cmd === "snapshot") snapshotOpeners();
  else if (cmd === "sync") syncClosers();
  else {
    console.error("Usage: node scripts/sync-line-movement.mjs [snapshot|sync]");
    process.exit(1);
  }
}
