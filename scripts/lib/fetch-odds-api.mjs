// fetch-odds-api.mjs — The Odds API (https://the-odds-api.com) spread ingest.
//
// Requires ODDS_API_KEY. Returns { games: [{ awayTeam, homeTeam, closingSpread, books? }] }
// compatible with sync-line-movement.mjs mergeExternalSpreads.

import { appendFileSync, writeFileSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";
import { fetchWithRetry } from "./retry.mjs";
import { abbrFromFullName } from "./team-names.mjs";

const ODDS_API = "https://api.the-odds-api.com/v4/sports/basketball_nba/odds";
const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "../..");
const BOOKS_TS = join(ROOT, "client/src/lib/oddsBooksData.ts");

/** Append Odds API quota to GITHUB_STEP_SUMMARY when running in CI. */
export function appendOddsQuotaSummary({ remainingRequests, gamesCount, note } = {}) {
  const summaryPath = process.env.GITHUB_STEP_SUMMARY;
  if (!summaryPath) return;

  const lines = [
    "",
    "### The Odds API — quota",
    "",
    "| Metric | Value |",
    "| --- | --- |",
    `| Requests remaining | ${remainingRequests ?? "n/a"} |`,
    `| Spreads parsed | ${gamesCount ?? "n/a"} |`,
  ];
  if (note) lines.push("", note);
  lines.push("");
  appendFileSync(summaryPath, lines.join("\n"));
}

function formatSpread(homeAbbr, awayAbbr, homeName, awayName, homePoint, awayPoint) {
  const favoriteAbbr = homePoint < awayPoint ? homeAbbr : awayAbbr;
  const favoritePoint = Math.min(homePoint, awayPoint);
  return `${favoriteAbbr} ${favoritePoint > 0 ? "+" : ""}${favoritePoint}`;
}

/**
 * Parse all US bookmaker spreads; closingSpread = first book (stable), books[] = consensus list.
 * @param {object} event
 * @returns {{ awayTeam: string, homeTeam: string, closingSpread: string, books: object[] } | null}
 */
export function parseOddsEvent(event) {
  const homeName = event.home_team;
  const awayName = event.away_team;
  const homeAbbr = abbrFromFullName(homeName);
  const awayAbbr = abbrFromFullName(awayName);
  if (!homeAbbr || !awayAbbr) return null;

  const bookmakers = event.bookmakers ?? [];
  const books = [];
  for (const book of bookmakers) {
    const market = (book.markets ?? []).find((m) => m.key === "spreads");
    if (!market?.outcomes?.length) continue;

    const byName = new Map(market.outcomes.map((o) => [o.name, o.point]));
    const homePoint = byName.get(homeName);
    const awayPoint = byName.get(awayName);
    if (homePoint == null || awayPoint == null) continue;

    const spread = formatSpread(homeAbbr, awayAbbr, homeName, awayName, homePoint, awayPoint);
    books.push({
      key: String(book.key ?? "book"),
      title: String(book.title ?? book.key ?? "Book"),
      spread,
    });
  }

  if (!books.length) return null;
  return {
    awayTeam: awayAbbr,
    homeTeam: homeAbbr,
    closingSpread: books[0].spread,
    books,
  };
}

/** Write client module for Betting Intel multi-book UI. Preserve seed when empty. */
export function writeOddsBooksClientTs(games) {
  const list = Array.isArray(games) ? games.filter((g) => Array.isArray(g.books) && g.books.length) : [];
  if (list.length === 0) {
    console.log("⏭ oddsBooksData.ts unchanged (no multi-book rows)");
    return;
  }
  const body = list
    .map((g) => {
      const books = g.books
        .map(
          (b) =>
            `      { key: ${JSON.stringify(b.key)}, title: ${JSON.stringify(b.title)}, spread: ${JSON.stringify(b.spread)} }`,
        )
        .join(",\n");
      return `  {\n    awayTeam: ${JSON.stringify(g.awayTeam)},\n    homeTeam: ${JSON.stringify(g.homeTeam)},\n    closingSpread: ${JSON.stringify(g.closingSpread)},\n    books: [\n${books}\n    ],\n  }`;
    })
    .join(",\n");

  const content = `// AUTO-GENERATED by scripts/lib/fetch-odds-api.mjs — do not edit by hand.

export interface OddsBookQuote {
  key: string;
  title: string;
  spread: string;
}

export interface OddsBooksGame {
  awayTeam: string;
  homeTeam: string;
  closingSpread: string;
  books: OddsBookQuote[];
}

export const oddsBooksGames: OddsBooksGame[] = [
${body}
];
`;
  writeFileSync(BOOKS_TS, content, "utf8");
  console.log(`✅ Wrote ${list.length} multi-book row(s) to oddsBooksData.ts`);
}

/**
 * @returns {Promise<{ games: object[], source: string, fetchedAt: string, remainingRequests?: string } | null>}
 */
export async function fetchOddsApiSpreads(apiKey) {
  if (!apiKey?.trim()) return null;

  const url = new URL(ODDS_API);
  url.searchParams.set("apiKey", apiKey.trim());
  url.searchParams.set("regions", "us");
  url.searchParams.set("markets", "spreads");
  url.searchParams.set("oddsFormat", "american");

  const res = await fetchWithRetry(url.toString(), {}, { maxRetries: 2, baseDelayMs: 1500 });
  const remaining = res.headers.get("x-requests-remaining") ?? undefined;
  const events = await res.json();

  if (!Array.isArray(events)) {
    throw new Error(typeof events?.message === "string" ? events.message : "Unexpected Odds API response");
  }

  const games = [];
  for (const event of events) {
    const row = parseOddsEvent(event);
    if (row) games.push(row);
  }

  const payload = {
    games,
    source: "the-odds-api",
    fetchedAt: new Date().toISOString(),
    remainingRequests: remaining,
  };

  if (process.env.GITHUB_ACTIONS === "true") {
    appendOddsQuotaSummary({
      remainingRequests: remaining,
      gamesCount: games.length,
    });
  }

  writeOddsBooksClientTs(games);
  return payload;
}
