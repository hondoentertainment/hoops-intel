#!/usr/bin/env node
// generate-community-pulse.mjs — Weekly Community Pulse generator
// Aggregates community rankings and sentiment using Claude AI
// Run weekly on Monday 6am PST via GitHub Actions (.github/workflows/weekly-update.yml)

import Anthropic from "@anthropic-ai/sdk";
import { readFileSync, writeFileSync } from "fs";
import { fileURLToPath } from "url";
import { dirname, join } from "path";

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const ROOT = join(__dirname, "..");

// ── Date helpers ───────────────────────────────────────────
function toISODate(daysOffset = 0) {
  const d = new Date();
  const la = new Date(d.toLocaleString("en-US", { timeZone: "America/Los_Angeles" }));
  la.setDate(la.getDate() + daysOffset);
  return la.toISOString().slice(0, 10);
}

function toDisplayDate(daysOffset = 0) {
  const d = new Date();
  const la = new Date(d.toLocaleString("en-US", { timeZone: "America/Los_Angeles" }));
  la.setDate(la.getDate() + daysOffset);
  return la.toLocaleDateString("en-US", { month: "long", day: "numeric", year: "numeric" });
}

function defaultWeekLabel() {
  const now = new Date(new Date().toLocaleString("en-US", { timeZone: "America/Los_Angeles" }));
  const dayOfWeek = now.getDay();
  const daysToMon = dayOfWeek === 0 ? -6 : 1 - dayOfWeek;
  const mon = new Date(now);
  mon.setDate(now.getDate() + daysToMon);
  const sun = new Date(mon);
  sun.setDate(mon.getDate() + 6);

  const fmt = (d) =>
    d.toLocaleDateString("en-US", { month: "long", day: "numeric" });
  const year = sun.getFullYear();
  return `Week of ${fmt(mon)}–${fmt(sun).replace(/\D.*/, sun.getDate().toString())}, ${year}`;
}

// ── Read pulse context ─────────────────────────────────────
function readPulseContext() {
  try {
    return readFileSync(join(ROOT, "client/src/lib/pulseData.ts"), "utf8").slice(0, 4000);
  } catch {
    return "(pulseData.ts not available)";
  }
}

// ── Main ──────────────────────────────────────────────────
async function main() {
  const client = new Anthropic();

  const todayISO = toISODate(0);
  const todayDisplay = toDisplayDate(0);
  const weekLabel = defaultWeekLabel();

  console.log(`🗳️  Generating Community Pulse for ${weekLabel}`);

  const pulseContext = readPulseContext();
  console.log("📊 Loaded pulseData.ts context");

  // ── Generate communityPulseData.ts ──────────────────────
  console.log("🤖 Calling Claude API to generate community pulse...");

  const prompt = `You are the Hoops Intel community analyst. Generate the weekly Community Pulse — representing how the NBA fan community ranks teams, players, and narratives this week.

## Current Pulse Context
${pulseContext}

## Week Info
- Week: ${weekLabel}
- Generated: ${todayDisplay}
- ISO: ${todayISO}

Generate a complete TypeScript file (communityPulseData.ts) with this EXACT structure:

// Auto-generated by generate-community-pulse.mjs
// ${weekLabel}

export interface CommunityTeamRanking {
  rank: number;
  prevRank: number;
  team: string;
  teamFull: string;
  record: string;
  approvalRating: number;
  fanMood: "ecstatic" | "optimistic" | "content" | "frustrated" | "furious";
  weekSummary: string;
  hotTopic: string;
}

export interface CommunityMVPRanking {
  rank: number;
  prevRank: number;
  player: string;
  team: string;
  supportPct: number;
  caseFor: string;
  caseAgainst: string;
}

export interface CommunityDebate {
  id: string;
  question: string;
  sideA: string;
  sideB: string;
  sideAPct: number;
  sideBPct: number;
  hotness: number;
  topComment: string;
}

export interface CommunityPulseData {
  generatedDate: string;
  displayDate: string;
  weekLabel: string;
  powerRankings: CommunityTeamRanking[];
  mvpRankings: CommunityMVPRanking[];
  debates: CommunityDebate[];
  fanFrustrationIndex: { team: string; teamFull: string; score: number; reason: string }[];
  weeklyNarrative: string;
}

export const communityPulseData: CommunityPulseData = {
  generatedDate: "${todayISO}",
  displayDate: "${todayDisplay}",
  weekLabel: "${weekLabel}",
  powerRankings: [ ... ],    // All 30 teams ranked
  mvpRankings: [ ... ],      // Top 10 MVP candidates
  debates: [ ... ],          // 5-6 hot community debates
  fanFrustrationIndex: [ ... ], // Top 10 most frustrated fanbases
  weeklyNarrative: "2-3 sentence overview of the community mood this week",
};

// Top 20 ratable players (consumed by CommunityPulse.tsx). MUST be present.
export const ratablePlayers = [
  { player: "...", team: "..." },
  // ...20 entries total
];

## Guidelines
- powerRankings: Rank all 30 NBA teams. approvalRating 0-100 (fan approval of team direction).
- mvpRankings: Top 10 MVP candidates. supportPct should roughly sum to ~100 across top 5.
- debates: Current hot-button debates in the NBA community. hotness 1-100.
- sideAPct + sideBPct should equal 100.
- fanFrustrationIndex: score 0-100 (100 = maximum frustration). Focus on underperforming or controversial teams.
- prevRank: plausible previous week's rank (can differ by 1-5 spots).
- All text should feel like authentic fan/community voice.
- Base rankings on actual current standings and narratives from pulseData.
- ratablePlayers MUST be exported with exactly 20 entries (top stars across the league). This export is consumed by the CommunityPulse page and the build will fail without it.

Output ONLY the complete TypeScript file. No markdown fences, no explanation.`;

  const msg = await client.messages.create({
    model: "claude-sonnet-4-6",
    max_tokens: 16384,
    messages: [{ role: "user", content: prompt }],
  });

  let content = msg.content[0].text.trim();

  // Strip accidental markdown fences
  content = content
    .replace(/^```(?:typescript|ts)?\n?/m, "")
    .replace(/\n?```$/m, "")
    .trim();

  const requiredExports = ["communityPulseData", "ratablePlayers"];
  const missing = requiredExports.filter((e) => !new RegExp(`export\\s+const\\s+${e}\\b`).test(content));
  if (missing.length > 0) {
    console.error(`❌ communityPulseData.ts is missing exports: ${missing.join(", ")}`);
    console.error("   These exports are imported by CommunityPulse.tsx — build would fail.");
    process.exit(1);
  }

  writeFileSync(join(ROOT, "client/src/lib/communityPulseData.ts"), content, "utf8");
  console.log("✓ communityPulseData.ts written");
  console.log(`\n✅ Community Pulse complete for ${weekLabel}`);
}

main().catch((err) => {
  console.error("❌ Generation failed:", err);
  process.exit(1);
});
